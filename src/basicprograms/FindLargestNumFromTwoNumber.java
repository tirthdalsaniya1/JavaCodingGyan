package basicprograms;

public class FindLargestNumFromTwoNumber {

	public static void main(String[] args) {

		int num1 = 15, num2 = 15;
		if (num1 - num2 > 0) {
			System.out.println("num1 is largest");
		} else if (num1 == num2) {
			System.out.println("both are same");

		} else {
			System.out.println("num2 is largest");

		}
	}

}
