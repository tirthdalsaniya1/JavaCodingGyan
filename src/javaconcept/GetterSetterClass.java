package javaconcept;

public class GetterSetterClass {

	private int price;
	private String name;
	private String color;

	public int getPrice() {
		return price;

	}

	public String getName() {
		return name;

	}

	public String getColor() {
		return color;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public void setName(String name) {
		this.name = name;
	}

}
